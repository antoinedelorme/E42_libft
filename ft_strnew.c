#include <string.h>
#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ptr;
	size_t	i;

	i = 0;
	if (!(ptr = malloc(size + 1)))
		return (NULL);
	while (i < (size + 1))
		ptr[i++] = '\0';
	return (ptr);
}
